package fengkongweishi.entity.personreport.po;

import javax.persistence.Entity;
import javax.persistence.Table;

import fengkongweishi.entity.personreport.po.BaseAnalyseItem;
import fengkongweishi.enums.Color;

/**
 * 身份证信息po对象
 * @author liuzhenfeng
 * @date 2018/2/26
 */
@Entity
@Table(name = "po_id_card")
public class IdCardPO extends BaseAnalyseItem{
	   /**
     * 姓名
     */
    private String name;
    /**
     * 性别
     */
    private String sex;
    /**
     * 年龄
     */
    private Integer age;
    /**
     * 出身地省
     */
    private String province;
    /**
     * 出身地市
     */
    private String city;
    /**
     * 出身地县区
     */
    private String town;
    /**
     * 地址
     */
    private String address;
    /**
     * 身份证
     */
    private String idCard;
    
    
    
	public IdCardPO() {
		super();
	}
	
	
	public IdCardPO(Color color) {
		super(color);
	}

	
	public String getIdCard() {
		return idCard;
	}


	public void setIdCard(String idCard) {
		this.idCard = idCard;
	}


	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public Integer getAge() {
		return age;
	}
	public void setAge(Integer age) {
		this.age = age;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getTown() {
		return town;
	}
	public void setTown(String town) {
		this.town = town;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
    
    

}
