package fengkongweishi.entity.personreport.po;

import fengkongweishi.enums.Color;

import javax.persistence.Entity;
import javax.persistence.Table;

/**
 * 学历信息po对象
 *
 * @author huanghengkun
 * @date 2018/02/26
 */
@Entity
@Table(name = "po_education")
public class EducationPO extends BaseAnalyseItem {
    /**
     * 姓名
     */
    private String name;
    /**
     * 毕业结论
     */
    private String studyResult;
    /**
     * 毕业时间
     */
    private String graduateTime;
    /**
     * 学校
     */
    private String college;
    /**
     * 学历
     */
    private String degree;
    /**
     * 院校类型
     */
    private String collegeType;

    public EducationPO() {
    }

    public EducationPO(Color color) {
        super(color);
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStudyResult() {
        return studyResult;
    }

    public void setStudyResult(String studyResult) {
        this.studyResult = studyResult;
    }

    public String getGraduateTime() {
        return graduateTime;
    }

    public void setGraduateTime(String graduateTime) {
        this.graduateTime = graduateTime;
    }

    public String getCollege() {
        return college;
    }

    public void setCollege(String college) {
        this.college = college;
    }

    public String getDegree() {
        return degree;
    }

    public void setDegree(String degree) {
        this.degree = degree;
    }

    public String getCollegeType() {
        return collegeType;
    }

    public void setCollegeType(String collegeType) {
        this.collegeType = collegeType;
    }

}
